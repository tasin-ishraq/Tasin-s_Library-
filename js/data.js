function getWriters() {
    return Object.keys(writersData);
}

function getWriterIcon(writerName) {
    // Check if custom icon is stored in localStorage
    const customIconKey = `icon_writer_${writerName}`;
    const customIcon = localStorage.getItem(customIconKey);
    if (customIcon) {
        return customIcon;
    }
    // Return default icon from data
    return writersData[writerName]?.icon || "📚";
}

// Check if writer has categories instead of direct books
function isWriterWithCategories(writerName) {
    return writersData[writerName]?.type === "categories";
}

// Get categories for a category-based writer
function getCategories(writerName) {
    if (!isWriterWithCategories(writerName)) return [];
    return Object.keys(writersData[writerName].categories || {});
}

// Get books in a specific category
function getBooksInCategory(writerName, categoryName) {
    if (!isWriterWithCategories(writerName)) return [];
    return writersData[writerName]?.categories?.[categoryName] || [];
}

// Get direct books (for non-category writers)
function getWriterBooks(writerName) {
    if (isWriterWithCategories(writerName)) return [];
    return writersData[writerName]?.books || [];
}

// Get a specific book (updated to support both types)
function getBook(writerName, bookTitle, categoryName = null) {
    let books = [];

    if (categoryName) {
        // Book is in a category
        books = getBooksInCategory(writerName, categoryName);
    } else {
        // Direct book
        books = getWriterBooks(writerName);
    }

    return books.find(b => b.title === bookTitle);
}

